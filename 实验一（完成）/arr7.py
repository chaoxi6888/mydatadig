import numpy as np

# 一维数组
a = np.array([1, 2, 3])

# 二维数组
b = np.array([[4, 5, 6],
              [7, 8, 9]])

# 加法
result_add = a + b
print("加法结果：\n", result_add)

# 减法
result_subtract = a - b
print("减法结果：\n", result_subtract)

# 乘法
result_multiply = a * b
print("乘法结果：\n", result_multiply)

# 除法
result_divide = a / b
print("除法结果：\n", result_divide)
